# config/settings.py
from pydantic_settings import BaseSettings
from typing import Optional


class Settings(BaseSettings):
    """
    إعدادات منصة SAIF.AI المركزية.
    كل القيم تُقرأ من ملف .env تلقائياً.
    """

    # ═══════════════════════════════════════════
    # المشروع
    # ═══════════════════════════════════════════
    APP_NAME: str = "SAIF.AI"
    APP_VERSION: str = "1.0.0"
    DEBUG: bool = True

    # ═══════════════════════════════════════════
    # قاعدة البيانات
    # ═══════════════════════════════════════════
    DATABASE_URL: str = "sqlite+aiosqlite:///./saif_ai.db"

    # ═══════════════════════════════════════════
    # Redis
    # ═══════════════════════════════════════════
    REDIS_URL: str = "redis://localhost:6379/0"

    # ═══════════════════════════════════════════
    # الذكاء الاصطناعي - OpenRouter
    # ═══════════════════════════════════════════
    OPENROUTER_API_KEY: Optional[str] = None
    OPENROUTER_DEFAULT_MODEL: str = "google/gemini-2.0-flash-lite-001"

    # ═══════════════════════════════════════════
    # الذكاء الاصطناعي - Gemini مباشر
    # ═══════════════════════════════════════════
    GEMINI_API_KEY: Optional[str] = None
    GEMINI_DEFAULT_MODEL: str = "gemini-2.0-flash"

    # ═══════════════════════════════════════════
    # المزود النشط (openrouter أو gemini)
    # ═══════════════════════════════════════════
    ACTIVE_PROVIDER: str = "openrouter"

    # ═══════════════════════════════════════════
    # الذاكرة
    # ═══════════════════════════════════════════
    SHORT_MEMORY_SIZE: int = 20     # عدد آخر رسائل يتم تذكرها
    SUMMARY_TRIGGER: int = 15       # بعد كم رسالة نحدث الملخص

    # ═══════════════════════════════════════════
    # الكاش
    # ═══════════════════════════════════════════
    CACHE_TTL: int = 3600           # مدة صلاحية الكاش بالثواني (ساعة)

    # ═══════════════════════════════════════════
    # شخصيات المنصة
    # ═══════════════════════════════════════════
    DEFAULT_PERSONA: str = (
        "أنت مساعد SAIF.AI الذكي والمحترف. "
        "خبير في جميع المجالات، تجيب بدقة وإحترافية وباللغة العربية الفصحى. "
        "تستخدم أسلوباً ودوداً ومحترماً."
    )

    # ═══════════════════════════════════════════
    # الأمان
    # ═══════════════════════════════════════════
    RATE_LIMIT_PER_MINUTE: int = 30  # أقصى عدد طلبات في الدقيقة للمستخدم الواحد

    # ═══════════════════════════════════════════
    # ميزات المنصة
    # ═══════════════════════════════════════════
    THINKING_MODE_ENABLED: bool = True    # وضع التفكير
    IMAGE_GEN_ENABLED: bool = True        # إنشاء الصور
    VOICE_ENABLED: bool = True            # تحويل الصوت لنص
    FILE_UPLOAD_ENABLED: bool = True      # رفع الملفات
    CAMERA_ENABLED: bool = True           # فتح الكاميرا
    DARK_MODE_DEFAULT: bool = True        # الوضع الليلي افتراضي

    class Config:
        env_file = ".env"
        env_file_encoding = "utf-8"


# نسخة واحدة من الإعدادات يتم استيرادها في أي مكان
settings = Settings()